// Calculando trapézio
public class Atribuicao_de_expressoes_mais_complexa {

	public static void main(String[] args) {
		// Variaveis
		double base_menor, base_maior, altura, area;
		
		// Atribuições
		base_menor = 6.0;
		base_maior = 8.0;
		altura = 5.0;
		
		// Atribuição da expressão área
		area = (base_menor + base_maior) / 2.0 * altura;
		
		// Impressão da área do trapézio
		System.out.print(area);
		
	}

}
